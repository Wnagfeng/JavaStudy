public class Animal {
    private String age;
    private String color;

    public Animal() {
    }

    public Animal(String age, String color) {
        this.age = age;
        this.color = color;
    }

    public String getAge() {
        return age;
    }

    public String getColor() {
        return color;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
